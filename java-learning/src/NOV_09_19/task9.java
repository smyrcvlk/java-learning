package NOV_09_19;

public class task9 {
    public static void main(String[] args) {
        //write a java program that iterates an array using while-loop
        int[] intArray = {100, 2, 4, 5, 6, 7, 8, 9, 10};

        int i = 0;
        while (i < intArray.length) {
            System.out.print(intArray[i] + " ");
            i++;
        }



    }



}
